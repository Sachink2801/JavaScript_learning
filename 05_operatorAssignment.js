console.log("========== compound addition +=  ================");
num1=10;
num2=20;
num1+=num2;  //num1=num1+num2
console.log(`compound addition += is :${num1}`);

console.log("=========== compound subtraction -= =============");
num1=50;
num2=40;
num1-=num2;
console.log(`compound subtraction -= :${num1}`);

console.log("=========== compound Multiplication *= ===========");
num1=50;
num2=50;
num1*=num2;
console.log(`compound Multiplication *=: ${num1}`);

console.log("=========== compound Division /= ===========");
num1=50;
num2=50;
num1/=num2;
console.log(`compound Division /=: ${num1}`);

console.log("=========== compound Modulus %= ===========");
num1=50;
num2=100;
num1%=num2;
console.log(`compound Multiplication %=: ${num1}`);



